void USART2_IRQHandler(void)
{
	u8 c;
	if(USART_GetITStatus(USART2, USART_IT_RXNE) != RESET)
	{
		LED2(ON); 	
	    c=USART2->DR;
//	  	printf("%c",c);    //将接受到的数据直接返回打印
		switch(c)
		{
			case '1':
				printf("1\n");
				incrsSVM_Th1();
				printf("a");
				break;
			case '2':
				printf("2\n");
				incrsAng_Th1();
				incrsSVM_Th2();
				incrsSVM_Th3();
				printf("a");
				break;
			case '3':
				printf("3\n");
				printf("a");
				break;
			case '4':
				printf("4\n");
				printf("a");
				break;
			case '5':
				printf("5\n");
				printf("a");
				break;
			case '6':
				restoreSVM_Th();
				restoreAng_Th();
				printf("a");
				break;
			default:				
				break;
		}
		resetStop();
		LED2(OFF);
	}
}

